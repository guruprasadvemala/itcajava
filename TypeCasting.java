package day2;

public class TypeCasting {
	public static void main(String args[])
	{
		double d=12.22;
		int i=9;
		System.out.println("id="+d);
		System.out.println("i="+i);
		System.out.println("After type casting");
		i=(int)d;
		System.out.println("id="+d);
		System.out.println("i="+i);
	}

}
